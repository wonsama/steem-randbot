/**
 * 개체에서 키을 제외하고 값만 목록에 담아 넘겨준다
 * 리턴되는 값은 정렬을 해줘야 됨에 유의
 * @param {Object} obj 개체 정보
 * @returns Array
 */
function objToArr(obj) {
  return Object.entries(obj).map((x) => {
    return x[1];
  });
}

